-- 1. Xóa các bảng cũ nếu tồn tại (theo thứ tự ngược lại của FK)
DROP TABLE IF EXISTS employees_approving;
DROP TABLE IF EXISTS accounts_approving;
DROP TABLE IF EXISTS test_drives;
DROP TABLE IF EXISTS service_histories;
DROP TABLE IF EXISTS car_transfers;
DROP TABLE IF EXISTS payments;
DROP TABLE IF EXISTS contracts;
DROP TABLE IF EXISTS deposits;
DROP TABLE IF EXISTS quotations;
DROP TABLE IF EXISTS customers;
DROP TABLE IF EXISTS cars;
DROP TABLE IF EXISTS car_models;
DROP TABLE IF EXISTS employees;
DROP TABLE IF EXISTS accounts;
DROP TABLE IF EXISTS showrooms;

-- 2. Tạo cấu trúc bảng

-- 1. Showrooms (Quản lý các chi nhánh/Showroom)
CREATE TABLE showrooms (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, -- Khóa chính, tự động tăng
    name VARCHAR(200) NOT NULL,                           -- Tên showroom
    address VARCHAR(500) NOT NULL,                        -- Địa chỉ chi tiết
    phone VARCHAR(20),                                    -- Số điện thoại liên lạc
    email VARCHAR(150),                                   -- Email của showroom
    manager VARCHAR(200),                                 -- Tên người quản lý showroom
    status VARCHAR(20) DEFAULT 'ACTIVE',                  -- Trạng thái hoạt động (ACTIVE, INACTIVE)
    created_at TIMESTAMP,                                 -- Thời điểm tạo bản ghi
    updated_at TIMESTAMP,                                 -- Thời điểm cập nhật bản ghi gần nhất
    created_by VARCHAR(100),                              -- Người tạo bản ghi
    updated_by VARCHAR(100)                               -- Người cập nhật bản ghi gần nhất
);

-- 2. Accounts (Tài khoản người dùng hệ thống)
CREATE TABLE accounts (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, -- Khóa chính
    username VARCHAR(100) NOT NULL UNIQUE CHECK (username ~ '^[a-zA-Z0-9]+$'), -- Tên đăng nhập (chữ và số)
    password VARCHAR(255) NOT NULL,                       -- Mật khẩu đã mã hóa
    email VARCHAR(150) NOT NULL UNIQUE,                   -- Email người dùng (duy nhất)
    full_name VARCHAR(150),                               -- Họ và tên đầy đủ
    roles TEXT,                                           -- Danh sách vai trò (ADMIN, SALES, KHO, KE_TOAN...)
    status VARCHAR(20) NOT NULL,                          -- Trạng thái tài khoản (ACTIVE, LOCKED, INACTIVE)
    failed_login_attempts INT DEFAULT 0,                  -- Số lần đăng nhập sai liên tiếp
    created_at TIMESTAMP,
    updated_at TIMESTAMP,
    created_by VARCHAR(100),
    updated_by VARCHAR(100)
);

-- 3. Employees (Thông tin nhân viên chi tiết)
CREATE TABLE employees (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, -- Khóa chính
    full_name VARCHAR(150) NOT NULL,                      -- Họ và tên nhân viên
    phone VARCHAR(20),                                    -- Số điện thoại cá nhân
    email VARCHAR(150),                                   -- Email cá nhân
    address VARCHAR(500),                                 -- Địa chỉ thường trú
    birth_date DATE,                                      -- Ngày sinh
    gender VARCHAR(10),                                   -- Giới tính
    national_id VARCHAR(20),                              -- Số CCCD/CMND
    join_date DATE,                                       -- Ngày vào làm
    employee_status VARCHAR(20) DEFAULT 'ACTIVE',         -- Trạng thái nhân viên (ACTIVE, LEAVED)
    showroom_id BIGINT,                                   -- Showroom nơi nhân viên làm việc (FK)
    username VARCHAR(100) UNIQUE,                         -- Tên đăng nhập hệ thống (FK)
    created_at TIMESTAMP,
    updated_at TIMESTAMP,
    created_by VARCHAR(100),
    updated_by VARCHAR(100),
    CONSTRAINT fk_employee_showroom FOREIGN KEY (showroom_id) REFERENCES showrooms(id),
    CONSTRAINT fk_employee_account FOREIGN KEY (username) REFERENCES accounts(username)
);

-- 4. Car Models (Danh mục các dòng xe/mẫu xe)
CREATE TABLE car_models (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, -- Khóa chính
    name VARCHAR(200) NOT NULL,                           -- Tên mẫu xe (ví dụ: Toyota Camry)
    manufacturer VARCHAR(100),                            -- Hãng sản xuất (Toyota, Honda...)
    series VARCHAR(100),                                  -- Dòng xe (Sedan, SUV...)
    year VARCHAR(50),                                     -- Đời xe/Năm sản xuất
    color VARCHAR(100),                                   -- Màu sắc mẫu
    car_type VARCHAR(50),                                 -- Loại xe (Số sàn, số tự động)
    engine VARCHAR(100),                                  -- Thông số động cơ
    transmission VARCHAR(50),                             -- Loại hộp số
    listed_price DECIMAL(20, 2),                          -- Giá niêm yết của hãng
    description TEXT,                                     -- Mô tả chi tiết tính năng
    status VARCHAR(20) DEFAULT 'ACTIVE',                  -- Trạng thái kinh doanh (ACTIVE, STOPPED)
    created_at TIMESTAMP,
    updated_at TIMESTAMP,
    created_by VARCHAR(100),
    updated_by VARCHAR(100)
);

-- 5. Cars (Danh sách từng xe vật lý cụ thể)
CREATE TABLE cars (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, -- Khóa chính
    chassis_number VARCHAR(50) NOT NULL UNIQUE,           -- Số khung (VIN) - duy nhất
    engine_number VARCHAR(50) NOT NULL UNIQUE,            -- Số máy - duy nhất
    license_plate VARCHAR(20),                            -- Biển số xe (nếu có)
    car_model_id BIGINT NOT NULL,                         -- Liên kết tới mẫu xe (FK)
    showroom_id BIGINT NOT NULL,                          -- Showroom đang lưu kho xe này (FK)
    status VARCHAR(50) NOT NULL,                          -- Trạng thái (AVAILABLE, DEPOSITED, SOLD, TRANSFERRING)
    sale_price DECIMAL(20, 2),                           -- Giá bán thực tế áp dụng cho xe này
    arrival_date DATE,                                    -- Ngày nhập xe về kho
    year INT,                                             -- Năm sản xuất thực tế
    notes TEXT,                                           -- Ghi chú về tình trạng xe
    created_at TIMESTAMP,
    updated_at TIMESTAMP,
    created_by VARCHAR(100),
    updated_by VARCHAR(100),
    CONSTRAINT fk_car_model FOREIGN KEY (car_model_id) REFERENCES car_models(id),
    CONSTRAINT fk_car_showroom FOREIGN KEY (showroom_id) REFERENCES showrooms(id)
);

-- 6. Customers (Hồ sơ khách hàng)
CREATE TABLE customers (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, -- Khóa chính
    full_name VARCHAR(150) NOT NULL,                      -- Họ tên khách hàng
    phone VARCHAR(20),                                    -- Số điện thoại (dùng để tra cứu)
    email VARCHAR(150),                                   -- Email khách hàng
    address VARCHAR(500),                                 -- Địa chỉ liên lạc
    birth_date DATE,                                      -- Ngày sinh
    national_id VARCHAR(20),                              -- Số CCCD/CMND
    source_type VARCHAR(100),                             -- Nguồn khách hàng (Walk-in, Online, Facebook...)
    car_interest VARCHAR(500),                            -- Các mẫu xe khách hàng quan tâm
    assigned_employee_id BIGINT,                          -- Nhân viên Sale phụ trách chăm sóc (FK)
    notes TEXT,                                           -- Ghi chú đặc điểm khách hàng
    created_at TIMESTAMP,
    updated_at TIMESTAMP,
    created_by VARCHAR(100),
    updated_by VARCHAR(100),
    CONSTRAINT fk_customer_employee FOREIGN KEY (assigned_employee_id) REFERENCES employees(id)
);

-- 7. Quotations (Bảng dự toán kinh phí cho khách hàng)
CREATE TABLE quotations (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, -- Khóa chính
    customer_id BIGINT NOT NULL,                          -- Khách hàng nhận báo giá (FK)
    car_id BIGINT NOT NULL,                               -- Xe được báo giá (FK)
    employee_id BIGINT,                                   -- Nhân viên lập báo giá (FK)
    car_price DECIMAL(20, 2),                             -- Giá xe trong báo giá
    accessories DECIMAL(20, 2),                           -- Tổng chi phí phụ kiện kèm theo
    promotion DECIMAL(20, 2),                             -- Giá trị khuyến mãi/giảm giá
    other_costs DECIMAL(20, 2),                           -- Các chi phí khác (thuế, phí biển số...)
    total_amount DECIMAL(20, 2),                          -- Tổng giá trị dự toán cuối cùng
    created_date TIMESTAMP NOT NULL,                      -- Ngày lập dự toán
    status VARCHAR(50) NOT NULL,                          -- Trạng thái (PENDING, APPROVED, REJECTED)
    notes TEXT,                                           -- Ghi chú báo giá
    created_at TIMESTAMP,
    updated_at TIMESTAMP,
    created_by VARCHAR(100),
    updated_by VARCHAR(100),
    CONSTRAINT fk_quotation_customer FOREIGN KEY (customer_id) REFERENCES customers(id),
    CONSTRAINT fk_quotation_car FOREIGN KEY (car_id) REFERENCES cars(id),
    CONSTRAINT fk_quotation_employee FOREIGN KEY (employee_id) REFERENCES employees(id)
);

-- 8. Deposits (Giao dịch đặt cọc giữ xe)
CREATE TABLE deposits (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, -- Khóa chính
    quotation_id BIGINT,                                  -- Tham chiếu từ báo giá nào (nếu có)
    customer_id BIGINT NOT NULL,                          -- Khách hàng đặt cọc (FK)
    car_id BIGINT NOT NULL,                               -- Xe được đặt giữ (FK)
    employee_id BIGINT,                                   -- Nhân viên thu cọc (FK)
    amount DECIMAL(20, 2) NOT NULL,                       -- Số tiền đặt cọc
    deposit_date DATE NOT NULL,                           -- Ngày đặt cọc
    expiry_date DATE,                                     -- Ngày hết hạn giữ xe nếu không ký hợp đồng
    deposit_payment_method VARCHAR(50) NOT NULL,          -- Phương thức (CASH, BANK_TRANSFER)
    status VARCHAR(50) NOT NULL,                          -- Trạng thái (ACTIVE, COMPLETED, CANCELLED)
    notes TEXT,                                           -- Ghi chú về thỏa thuận cọc
    created_at TIMESTAMP,
    updated_at TIMESTAMP,
    created_by VARCHAR(100),
    updated_by VARCHAR(100),
    CONSTRAINT fk_deposit_quotation FOREIGN KEY (quotation_id) REFERENCES quotations(id),
    CONSTRAINT fk_deposit_customer FOREIGN KEY (customer_id) REFERENCES customers(id),
    CONSTRAINT fk_deposit_car FOREIGN KEY (car_id) REFERENCES cars(id),
    CONSTRAINT fk_deposit_employee FOREIGN KEY (employee_id) REFERENCES employees(id)
);

-- 9. Contracts (Hợp đồng mua bán xe chính thức)
CREATE TABLE contracts (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, -- Khóa chính
    contract_number VARCHAR(50) NOT NULL UNIQUE,          -- Số hợp đồng (duy nhất)
    customer_id BIGINT NOT NULL,                          -- Bên mua (FK)
    car_id BIGINT NOT NULL,                               -- Đối tượng xe mua bán (FK)
    employee_id BIGINT,                                   -- Nhân viên Sale phụ trách hợp đồng (FK)
    deposit_id BIGINT,                                    -- Tham chiếu phiếu đặt cọc tương ứng (FK)
    sign_date DATE NOT NULL,                              -- Ngày ký hợp đồng
    expected_delivery_date DATE,                          -- Ngày dự kiến giao xe
    actual_delivery_date DATE,                            -- Ngày thực tế bàn giao xe cho khách
    contract_value DECIMAL(20, 2) NOT NULL,               -- Tổng giá trị hợp đồng
    paid_amount DECIMAL(20, 2) DEFAULT 0,                 -- Số tiền khách đã thanh toán
    remaining_amount DECIMAL(20, 2),                      -- Số tiền còn nợ lại
    status VARCHAR(50) NOT NULL,                          -- Trạng thái (DRAFT, SIGNED, COMPLETED, CANCELLED)
    notes TEXT,                                           -- Ghi chú hợp đồng
    created_at TIMESTAMP,
    updated_at TIMESTAMP,
    created_by VARCHAR(100),
    updated_by VARCHAR(100),
    CONSTRAINT fk_contract_customer FOREIGN KEY (customer_id) REFERENCES customers(id),
    CONSTRAINT fk_contract_car FOREIGN KEY (car_id) REFERENCES cars(id),
    CONSTRAINT fk_contract_employee FOREIGN KEY (employee_id) REFERENCES employees(id),
    CONSTRAINT fk_contract_deposit FOREIGN KEY (deposit_id) REFERENCES deposits(id)
);

-- 10. Payments (Chi tiết các đợt thanh toán của hợp đồng)
CREATE TABLE payments (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, -- Khóa chính
    contract_id BIGINT NOT NULL,                         -- Hợp đồng thanh toán (FK)
    installment_number INT NOT NULL,                      -- Đợt thanh toán thứ mấy
    amount DECIMAL(20, 2) NOT NULL,                       -- Số tiền thanh toán đợt này
    payment_time TIMESTAMP NOT NULL,                      -- Thời điểm thực hiện thanh toán
    method VARCHAR(50) NOT NULL,                          -- Hình thức (CASH, BANK_TRANSFER, CREDIT_CARD)
    status VARCHAR(50) NOT NULL,                          -- Trạng thái (PENDING, CONFIRMED, FAILED)
    collector_id BIGINT,                                  -- Nhân viên kế toán/thu ngân xác nhận (FK)
    transaction_code VARCHAR(200),                        -- Mã giao dịch ngân hàng (nếu có)
    notes TEXT,                                           -- Ghi chú thanh toán
    created_at TIMESTAMP,
    updated_at TIMESTAMP,
    created_by VARCHAR(100),
    updated_by VARCHAR(100),
    CONSTRAINT fk_payment_contract FOREIGN KEY (contract_id) REFERENCES contracts(id),
    CONSTRAINT fk_payment_collector FOREIGN KEY (collector_id) REFERENCES employees(id)
);

-- 11. Car Transfers (Quản lý việc điều chuyển xe giữa các showroom chi nhánh)
CREATE TABLE car_transfers (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, -- Khóa chính
    car_id BIGINT NOT NULL,                               -- Xe cần điều chuyển (FK)
    from_showroom_id BIGINT NOT NULL,                     -- Showroom xuất (FK)
    to_showroom_id BIGINT NOT NULL,                       -- Showroom nhập (FK)
    created_by_employee_id BIGINT,                        -- Nhân viên tạo lệnh điều chuyển (FK)
    transfer_date DATE,                                   -- Ngày thực hiện chuyển đi
    expected_receive_date DATE,                           -- Ngày dự kiến tới nơi
    actual_receive_date DATE,                             -- Ngày thực tế nhận xe tại kho mới
    status VARCHAR(50) NOT NULL,                          -- Trạng thái (PENDING, SHIPPED, RECEIVED, CANCELLED)
    reason TEXT,                                          -- Lý do điều chuyển xe
    notes TEXT,                                           -- Ghi chú quá trình vận chuyển
    created_at TIMESTAMP,
    updated_at TIMESTAMP,
    created_by VARCHAR(100),
    updated_by VARCHAR(100),
    CONSTRAINT fk_transfer_car FOREIGN KEY (car_id) REFERENCES cars(id),
    CONSTRAINT fk_transfer_from_showroom FOREIGN KEY (from_showroom_id) REFERENCES showrooms(id),
    CONSTRAINT fk_transfer_to_showroom FOREIGN KEY (to_showroom_id) REFERENCES showrooms(id),
    CONSTRAINT fk_transfer_employee FOREIGN KEY (created_by_employee_id) REFERENCES employees(id)
);

-- 12. Service Histories (Lịch sử chăm sóc khách hàng / Nhật ký CRM)
CREATE TABLE service_histories (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, -- Khóa chính
    customer_id BIGINT NOT NULL,                          -- Khách hàng được chăm sóc (FK)
    employee_id BIGINT,                                   -- Nhân viên thực hiện chăm sóc (FK)
    service_date TIMESTAMP NOT NULL,                      -- Thời điểm tương tác với khách
    contact_type VARCHAR(20) NOT NULL,                    -- Hình thức (CALL, EMAIL, MEETING, ZALO...)
    content TEXT,                                         -- Nội dung cuộc trao đổi
    result TEXT,                                          -- Kết quả tương tác
    next_reminder_date TIMESTAMP,                         -- Ngày hẹn nhắc lại chăm sóc lần sau
    created_at TIMESTAMP,
    updated_at TIMESTAMP,
    created_by VARCHAR(100),
    updated_by VARCHAR(100),
    CONSTRAINT fk_service_customer FOREIGN KEY (customer_id) REFERENCES customers(id),
    CONSTRAINT fk_service_employee FOREIGN KEY (employee_id) REFERENCES employees(id)
);

-- 13. Test Drives (Quản lý đăng ký và thực hiện lái thử xe demo)
CREATE TABLE test_drives (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, -- Khóa chính
    customer_id BIGINT NOT NULL,                          -- Khách hàng đăng ký lái thử (FK)
    car_id BIGINT NOT NULL,                               -- Xe demo được chọn lái thử (FK)
    employee_id BIGINT,                                   -- Nhân viên sale đi kèm hỗ trợ (FK)
    showroom_id BIGINT,                                   -- Địa điểm thực hiện lái thử (FK)
    start_time TIMESTAMP NOT NULL,                        -- Thời điểm bắt đầu dự kiến
    end_time TIMESTAMP NOT NULL,                          -- Thời điểm kết thúc dự kiến
    status VARCHAR(50) NOT NULL,                          -- Trạng thái (SCHEDULED, COMPLETED, CANCELLED)
    notes TEXT,                                           -- Nhận xét của khách sau khi lái thử
    created_at TIMESTAMP,
    updated_at TIMESTAMP,
    created_by VARCHAR(100),
    updated_by VARCHAR(100),
    CONSTRAINT fk_test_customer FOREIGN KEY (customer_id) REFERENCES customers(id),
    CONSTRAINT fk_test_car FOREIGN KEY (car_id) REFERENCES cars(id),
    CONSTRAINT fk_test_employee FOREIGN KEY (employee_id) REFERENCES employees(id),
    CONSTRAINT fk_test_showroom FOREIGN KEY (showroom_id) REFERENCES showrooms(id)
);

-- 14. Accounts Approving (Quản lý phê duyệt thay đổi tài khoản)
CREATE TABLE accounts_approving (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, -- Khóa chính
    username VARCHAR(100) NOT NULL CHECK (username ~ '^[a-zA-Z0-9]+$'), -- Tên đăng nhập đề xuất (chữ và số)
    password VARCHAR(255) NOT NULL,                       -- Mật khẩu đề xuất
    email VARCHAR(150) NOT NULL,                          -- Email đề xuất
    full_name VARCHAR(150),                               -- Họ tên đề xuất
    status VARCHAR(50),                                   -- Trạng thái hiện tại của tài khoản (ACTIVE, INACTIVE...)
    roles TEXT,                                           -- Danh sách vai trò đề xuất
    status_approving VARCHAR(50) NOT NULL,                -- Trạng thái phê duyệt (AWAITING_APPROVAL, APPROVED, REJECTED)
    action VARCHAR(50) NOT NULL,                          -- Loại tác động (CREATE, UPDATE, DELETE, CHANGE_ROLES, CHANGE_PASSWORD, LOCK, UNLOCK, ACTIVE, INACTIVE)
    created_at TIMESTAMP,
    updated_at TIMESTAMP,
    created_by VARCHAR(100),
    updated_by VARCHAR(100)
);

-- 15. Employees Approving (Quản lý phê duyệt thay đổi thông tin nhân viên)
CREATE TABLE employees_approving (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, -- Khóa chính
    full_name VARCHAR(150) NOT NULL,                      -- Họ và tên nhân viên đề xuất
    phone VARCHAR(20),                                    -- Số điện thoại đề xuất
    email VARCHAR(150),                                   -- Email đề xuất
    address VARCHAR(500),                                 -- Địa chỉ đề xuất
    birth_date DATE,                                      -- Ngày sinh đề xuất
    gender VARCHAR(10),                                   -- Giới tính đề xuất
    national_id VARCHAR(20),                              -- Số CCCD/CMND đề xuất
    join_date DATE,                                       -- Ngày vào làm đề xuất
    employee_status VARCHAR(20) NOT NULL,                 -- Trạng thái nhân viên đề xuất (ACTIVE, LEAVED)
    showroom_id BIGINT,                                   -- Showroom đề xuất (FK)
    username VARCHAR(100),                                -- Tên đăng nhập hệ thống đề xuất (FK)
    action VARCHAR(20) NOT NULL,                          -- Loại tác động (CREATE, UPDATE, DELETE)
    status_approving VARCHAR(50) NOT NULL,                -- Trạng thái phê duyệt (AWAITING_APPROVAL, APPROVED, REJECTED)
    created_at TIMESTAMP,
    updated_at TIMESTAMP,
    created_by VARCHAR(100),
    updated_by VARCHAR(100),
    CONSTRAINT fk_employee_approving_showroom FOREIGN KEY (showroom_id) REFERENCES showrooms(id)
);

